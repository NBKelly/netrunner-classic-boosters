function corp_card_type(name) {
    let card_type = corp_cards[name];
    if(card_type == null)
	return 'operation';
    if(Number.isInteger(card_type))
	return 'agenda';
    return card_type;
}

function to_image_name(name) {
    let res = name.toLowerCase().replaceAll("(tm)", " tm").replaceAll("(r)", " r").replaceAll(" ", "-").replaceAll("'", "-").replaceAll('"', "").replaceAll(":", "");

    return ("resources/card-images/" + res + ".jpg");
}

function count_deck(map) {
    let count = 0;
    for (let [key, value] of map) {
	count += value;
    }

    return count;
}

function is_agenda(name) {
    return corp_card_type(name) == 'agenda';
}

function count_ap(map) {
    let count = 0;
    for (let [key, value] of map) {
	count += corp_cards[key]*value;
    }

    return count;
}

function ap_limit(numcards) {
    if(numcards <= 45)
	return "18-19";
    else {
	let qty = numcards - 44;
	let adj = 0;
	while(qty > 0) {
	    qty = qty - 5;
	    adj += 2;
	}

	let lownum = 18+adj;
	let highnum = 19 + adj;
	//return "18-19";
	return `${lownum}-${highnum}`
    }
}

var is_corp_card = function(name) {
    return corp_cards_array.includes(name);
}
